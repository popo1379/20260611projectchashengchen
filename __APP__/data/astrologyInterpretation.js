// 辅助函数
// =============================================
function pickRandom(arr) {
  if (!arr || arr.length === 0) return '';
  return arr[Math.floor(Math.random() * arr.length)];
}

function getSuggestion(domain) {
  var list = suggestionMap[domain] || suggestionMap['自我'];
  return pickRandom(list);
}

// =============================================
// 全局模板使用记录（去重机制）
// =============================================
var _usedTemplateKeys = {};

function _getTemplateKey(event) {
  var type = event.type || '';
  var mood = event.friendly ? 'pos' : 'neg';
  var transit = event.transitPlanet ? event.transitPlanet.name : '';
  var natal = event.natalPlanet ? event.natalPlanet.name : '';
  var house = event.house || '';
  return type + '|' + mood + '|' + transit + '|' + natal + '|' + house;
}

function _markTemplateUsed(event, templateIndex) {
  var key = _getTemplateKey(event) + '|' + templateIndex;
  _usedTemplateKeys[key] = true;
}

function _isTemplateUsed(event, templateIndex) {
  var key = _getTemplateKey(event) + '|' + templateIndex;
  return _usedTemplateKeys[key] === true;
}

function resetDailyCache() {
  _usedTemplateKeys = {};
}

// =============================================
// 核心：生成相位事件的描述（不含建议）
// =============================================
function generateAspectText(event) {
  if (!event || !event.transitPlanet || !event.natalPlanet) return null;

  var transitName = event.transitPlanet.name || '行星';
  var natalName = event.natalPlanet.name || '内心';
  var type = event.type;
  var isPositive = event.friendly !== false;

  var textLibrary;
  switch (type) {
    case '合相': textLibrary = conjunctionTexts; break;
    case '六分': textLibrary = sextileTexts; break;
    case '三分': textLibrary = trineTexts; break;
    case '刑': textLibrary = squareTexts; break;
    case '对分': textLibrary = oppositionTexts; break;
    default: return null;
  }

  var mood = isPositive ? 'positive' : 'challenging';
  var templateList = textLibrary[mood];
  if (!templateList || templateList.length === 0) return null;

  // 去重：从剩余未使用的模板中随机选择
  var unusedIndexes = [];
  for (var i = 0; i < templateList.length; i++) {
    if (!_isTemplateUsed(event, i)) {
      unusedIndexes.push(i);
    }
  }

  var templateIndex;
  if (unusedIndexes.length > 0) {
    templateIndex = unusedIndexes[Math.floor(Math.random() * unusedIndexes.length)];
  } else {
    return null;
  }

  var template = templateList[templateIndex];
  _markTemplateUsed(event, templateIndex);

  var text = template
    .replace(/\{transitPlanet\}/g, transitName)
    .replace(/\{natalPlanet\}/g, natalName)
    .replace(/\{transit\}/g, transitName)
    .replace(/\{natal\}/g, natalName);

  return text;
}

// =============================================
// 核心：生成宫位事件的描述（不含建议）
// =============================================
function generateHouseText(event) {
  if (!event || !event.house || !event.transitPlanet) return null;

  var house = event.house;
  var planetName = event.transitPlanet.name || '行星';

  var houseLibrary = houseTexts[house];
  if (!houseLibrary) return null;

  // 随机选择正面或困难语气
  var mood = Math.random() > 0.4 ? 'positive' : 'challenging';
  var templateList = houseLibrary[mood];
  if (!templateList || templateList.length === 0) return null;

  // 去重
  var unusedIndexes = [];
  for (var i = 0; i < templateList.length; i++) {
    if (!_isTemplateUsed(event, i)) {
      unusedIndexes.push(i);
    }
  }

  var templateIndex;
  if (unusedIndexes.length > 0) {
    templateIndex = unusedIndexes[Math.floor(Math.random() * unusedIndexes.length)];
  } else {
    return null;
  }

  var template = templateList[templateIndex];
  _markTemplateUsed(event, templateIndex);

  var text = template.replace(/\{planet\}/g, planetName);
  return text;
}

// =============================================
// 主函数：从事件列表生成一段完整运势
// 输出：horoscopeParagraphs = [ "一段完整文本" ]
// =============================================
function generateDailyHoroscope(events, dominantEvent) {
  // 重置当天的模板使用记录
  resetDailyCache();

  var eventTexts = [];       // 收集所有事件的描述
  var suggestions = [];      // 收集所有建议

  if (!events || events.length === 0) {
    return [
      '今天宇宙在安静地流动，没有重大的星象发生，但这恰恰是适合与自己相处的好日子。今天适合：静下来，感受当下，给那些平时被忽略的感受一点空间。'
    ];
  }

  // 1. 优先处理主导相位（最强影响力的事件）
  if (dominantEvent && dominantEvent.type !== 'house') {
    var text = generateAspectText(dominantEvent);
    if (text) {
      eventTexts.push(text);
      var domTransitName = dominantEvent.transitPlanet ? dominantEvent.transitPlanet.name : '自我';
      var domain = planetDomainMap[domTransitName] || domTransitName;
      suggestions.push(getSuggestion(domain));
    }
  }

  // 2. 处理其他事件（最多 2 个）
  var count = 0;
  var maxEvents = 2;

  for (var i = 0; i < events.length && count < maxEvents; i++) {
    var ev = events[i];
    if (ev === dominantEvent) continue;

    var text;
    if (ev.type === 'house') {
      text = generateHouseText(ev);
    } else {
      text = generateAspectText(ev);
    }

    if (text) {
      eventTexts.push(text);
      var transitName = ev.transitPlanet ? ev.transitPlanet.name : '自我';
      var d = planetDomainMap[transitName] || transitName;
      suggestions.push(getSuggestion(d));
      count++;
    }
  }

  // 如果没有任何事件文本，返回兜底段落
  if (eventTexts.length === 0) {
    return [
      '今天宇宙在安静地流动，你会愿意让自己感受当下。今天适合：给自己一点时间，不要急着做决定。'
    ];
  }

  // 3. 拼接成一段完整文本
  // 第一段直接输出，后续段用连接词串联
  var fullText = eventTexts[0];
  for (var j = 1; j < eventTexts.length; j++) {
    var connector = pickRandom(connectors);
    fullText = fullText + '。' + connector + eventTexts[j];
  }

  // 4. 在末尾添加建议（去重，去除空建议）
  var uniqueSuggestions = [];
  for (var k = 0; k < suggestions.length; k++) {
    if (suggestions[k] && uniqueSuggestions.indexOf(suggestions[k]) === -1) {
      uniqueSuggestions.push(suggestions[k]);
    }
  }

  if (uniqueSuggestions.length > 0) {
    fullText = fullText + '。今天适合：' + uniqueSuggestions.join('、') + '。';
  } else {
    fullText = fullText + '。';
  }

  return [fullText];
}

