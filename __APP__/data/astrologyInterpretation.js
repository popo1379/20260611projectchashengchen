// =============================================
// 获取行星在某星座的解读
// =============================================
function getPlanetInterpretation(planetName, signName) {
  if (planetName === '太阳') return sunInSign[signName] || '';
  if (planetName === '月亮') return moonInSign[signName] || '';
  if (planetName === '上升') return ascendantInSign[signName] || '';
  if (planetInSign[planetName]) {
    return planetInSign[planetName][signName] || '';
  }
  return '';
}

