// 权重配置
const weightConfig = {
  "上上签": { weight: 5, count: 5 },
  "上吉签": { weight: 25, count: 25 },
  "中吉签": { weight: 30, count: 40 },
  "中平签": { weight: 30, count: 30 },
  "下下签": { weight: 0, count: 0 }
};

// 获取指定场景的签列表
function getQianByScene(scene) {
  return qianList.filter(qian => qian.scene === scene || qian.scene === "综合");
}

// 获取指定等级的签列表
function getQianByLevel(level) {
  return qianList.filter(qian => qian.level === level);
}

// 根据权重随机抽取一支签
function drawRandomQian(scene) {
  const availableQian = getQianByScene(scene);
  const totalWeight = availableQian.reduce((sum, qian) => {
    return sum + weightConfig[qian.level].weight;
  }, 0);
  let random = Math.random() * totalWeight;
  for (let i = 0; i < availableQian.length; i++) {
    random -= weightConfig[availableQian[i].level].weight;
    if (random <= 0) {
      return availableQian[i];
    }
  }
  return availableQian[availableQian.length - 1];
}

